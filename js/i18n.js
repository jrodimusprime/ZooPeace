function t(key, vars = {}) {
  const pack = I18N.strings[I18N.current] || I18N.strings.en;
  let text = pack[key] ?? I18N.strings.en[key] ?? key;
  for (const [k, v] of Object.entries(vars)) {
    text = text.replaceAll(`{${k}}`, String(v));
  }
  return text;
}

function animalName(englishName) {
  if (I18N.current === 'en') return englishName;
  return I18N.animals[I18N.current]?.[englishName] || englishName;
}

function getLocalizedAnimal(index) {
  const animal = ANIMALS[index];
  if (!animal) return null;
  return { ...animal, displayName: animalName(animal.name) };
}

function setLanguage(lang) {
  if (!I18N.langs[lang]) lang = 'en';
  I18N.current = lang;
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang;
  applyStaticI18n();
}

function applyStaticI18n() {
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (key) el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (key) el.setAttribute('placeholder', t(key));
  });
  const title = document.querySelector('title');
  if (title) title.textContent = t('title');
}
